public class SuperInfo {
    private String superClassName;
    private String superClassPackage;

    public String getSuperClassName() {
        return superClassName;
    }

    public void setSuperClassName(final String superClassName) {
        this.superClassName = superClassName;
    }

    public String getSuperClassPackage() {
        return superClassPackage;
    }

    public void setSuperClassPackage(final String superClassPackage) {
        this.superClassPackage = superClassPackage;
    }
}
